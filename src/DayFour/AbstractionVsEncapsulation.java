package DayFour;

/*
✅ Abstraction → "What to show"
    Hides implementation details
    Achieved using:
        Abstract class
        Interface

interface ATM {
    void withdraw();
}

✅ Encapsulation → "How to protect"
    Wrapping data + methods together
    Achieved using:
        private variables
        getters/setters

class Bank {
    private int balance;

    public int getBalance() {
        return balance;
    }
}

🔥 Key Difference

| Abstraction                     | Encapsulation                 |
| ------------------------------- | ----------------------------- |
| Hides implementation            | Hides data                    |
| Design level                    | Code level                    |
| Achieved via interface/abstract | Achieved via access modifiers |
 */

