package DayFour.Packages;

/*
What are Access Modifiers?
👉 They define the scope of classes, methods, variables, and constructors.

1. private
    Accessible only within the same class
    Used for data hiding (encapsulation)

2. default (no modifier)
    Accessible within the same package only
    Also called package-private

3. protected
Accessible:
    Same package
    Subclass (even in different package)

4. public
    Accessible everywhere
 */


/*
Can we use access modifiers on class?
Only:
    public
    default
❌ private, protected not allowed for top-level classes


Which modifier is most restrictive?
👉 private

❓ Why use access modifiers?
        Encapsulation
        Security
        Maintainability
 */
